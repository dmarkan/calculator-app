const add = function add(n1, n2) {
    return n1 + n2;
  };
  
  const multiply = function multiply(n1, n2) {
    return n1 * n2;
  };
  
  const subtract = function subtract(n1, n2) {
    return n1 - n2;
  };
  
  const divide = function divide(n1, n2) {
    return n1 / n2;
  };
  
  export default { add, multiply, subtract, divide };
  